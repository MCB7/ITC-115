package assignment4;

public class Repetition {
	static void Rep1(String hello, int amount) {
		
		for (int i = 0 ; i < amount; i++) {
		    
		
		System.out.print(hello);
		
	}
	
	}
	public static void main(String[] args) {
		
		   
		Rep1("hello", 3);

	}

	}

